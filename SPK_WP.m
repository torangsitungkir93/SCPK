% --- Executes on button press in cari.
function cari_Callback(hObject, eventdata, handles)
% hObject    handle to cari (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
 x = [15,10000,90,75,80;25,20000,70,95,90;20,5500,50,60,40;35,15000,25,68,72;14,14000,56,60,79];
 k1 = str2num(get(handles.k1,'string'));
 k2 = str2num(get(handles.k2,'string'));
 k3 = str2num(get(handles.k3,'string'));
 k4 = str2num(get(handles.k4,'string'));
 k5 = str2num(get(handles.k5,'string'));
 k = [k1,k2,k3,k4,k5]; 
 w1 = str2num(get(handles.w1,'string'));
 w2 = str2num(get(handles.w2,'string'));
 w3 = str2num(get(handles.w3,'string'));
 w4 = str2num(get(handles.w4,'string'));
 w5 = str2num(get(handles.w5,'string'));
 w = [w1,w2,w3,w4,w5];
[m n] = size(x);
w=w./sum(w);

for j=1:n,
    if k(j) == 0, w(j)=-1*w(j);
    end;
end;
for i=1:m,
    S(i)=prod(x(i,:).^w);
end;

V = S/sum(S);
set(handles.hasil,'string',(V));
 
 
 
